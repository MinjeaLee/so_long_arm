#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	len;

	if (s == NULL)
	{
		write(fd, "\0", 1);
		return ;
	}
	len = ft_strlen(s);
	write(fd, s, len);
	write(fd, "\n", 1);
}
